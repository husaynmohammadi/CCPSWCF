#=============================================================================
#    Units (Outputs: m, N, Pa)
#=============================================================================
# Base Units:
s       = 1. 
m       = 1.
N       = 1.

# SI:
cm      = 0.01      *m
mm      = 0.001     *m

kN      = 1000      *N

Pa      = 1         *N/m**2
kPa     = 1000      *Pa
MPa     = 1000      *kPa
GPa     = 1000      *MPa

g       = 9.80665   *m/s**2
kg      = 1         *N/g
ton     = 1000      *kg

# US Customary System:

inch    = 25.4      *mm
ft      = 12        *inch

lb      = 4.44822   *N
kip     = 1000      *lb

psi     = 1         *lb/inch**2
psf     = 1         *lb/ft**2
ksi     = 1         *kip/inch**2
ksf     = 1         *kip/ft**2


