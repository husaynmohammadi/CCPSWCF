#=============================================================================
#    Units (Outputs: US-Based)
#=============================================================================
# Base Units:
s       = 1. 
inch    = 1.
kip     = 1.


# US Customary System:

ft      = 12        *inch

lb      = 0.001     *kip

psi     = 1         *lb/inch**2
psf     = 1         *lb/ft**2
ksi     = 1         *kip/inch**2
ksf     = 1         *kip/ft**2


# SI:
m       = 39.37007874016*inch
cm      = 0.01      *m
mm      = 0.001     *m

N       = 0.2248090795*lb
kN      = 1000      *N

Pa      = 1         *N/m**2
kPa     = 1000      *Pa
MPa     = 1000      *kPa
GPa     = 1000      *MPa

g       = 9.80665   *m/s**2
kg      = 1         *N/g
ton     = 1000      *kg




